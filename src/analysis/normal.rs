#[derive(Debug, Clone)]
#[allow(private_interfaces)]
pub(crate) enum MoleculeMembraneNormal {
    Static(Vector3D),
    Dynamic(DynamicMembraneNormal),
    Manual(ManualMembraneNormal),
}

impl From<&MembraneNormal> for MoleculeMembraneNormal {
    #[inline]
    fn from(value: &MembraneNormal) -> Self {
        match value {
            MembraneNormal::Static(axis) => Self::Static((*axis).into()),
            MembraneNormal::Dynamic(params) => {
                // requested collecting dynamic membrane normals
                let setup_storage = match params.collect() {
                    Collect::Boolean(false) => false,
                    Collect::Boolean(true) | Collect::File(_) => true,
                };

                Self::Dynamic(DynamicMembraneNormal {
                    heads: Vec::new(),
                    radius: params.radius(),
                    normals: Vec::new(),
                    storage: if setup_storage {
                        Some(Default::default())
                    } else {
                        None
                    },
                })
            }
            // will be set-up later
            MembraneNormal::FromFile(_) => Self::Manual(ManualMembraneNormal {
                normals: None,
                step: 1,
            }),
            MembraneNormal::FromMap(_) => Self::Manual(ManualMembraneNormal {
                normals: None,
                step: 1,
            }),
        }
    }
}

impl MoleculeMembraneNormal {
    /// Get membrane normal for the molecule with the target index.
    pub(super) fn get_normal<'a>(
        &mut self,
        frame_index: usize,
        molecule_index: usize,
        system: &'a System,
        pbc_handler: &'a impl PBCHandler<'a>,
    ) -> Result<&Vector3D, AnalysisError> {
        match self {
            Self::Static(vector) => Ok(vector),
            Self::Dynamic(dynamic) => dynamic.get_normal(molecule_index, system, pbc_handler),
            Self::Manual(manual) => manual.get_normal(frame_index, molecule_index),
        }
    }

    /// Insert a new molecule into the membrane normal structure.
    /// Does nothing if the membrane normal is static.
    #[inline(always)]
    pub(super) fn insert(&mut self, indices: &Group, system: &System) -> Result<(), TopologyError> {
        match self {
            Self::Static(_) | Self::Manual(_) => Ok(()),
            Self::Dynamic(dynamic) => dynamic.insert(indices, system),
        }
    }

    /// Initialize reading of the new frame.
    /// This resets the `normals` vector in the dynamic membrane normal variant.
    #[inline(always)]
    pub(super) fn init_new_frame(&mut self) {
        match self {
            Self::Static(_) | Self::Manual(_) => (),
            Self::Dynamic(dynamic) => dynamic.reset_normals(),
        }
    }

    /// Store membrane normals, if requested.
    #[inline(always)]
    pub(super) fn store_normals(&mut self) {
        match self {
            Self::Static(_) | Self::Manual(_) => (),
            Self::Dynamic(dynamic) => dynamic.store_normals(),
        }
    }
}

impl Add for MoleculeMembraneNormal {
    type Output = MoleculeMembraneNormal;

    fn add(self, rhs: Self) -> Self::Output {
        match (self, rhs) {
            // adding molecule membrane normals is only needed for dynamic membrane normals
            (MoleculeMembraneNormal::Dynamic(x), MoleculeMembraneNormal::Dynamic(y)) => {
                MoleculeMembraneNormal::Dynamic(x + y)
            }
            (other, _) => other,
        }
    }
}

#[derive(Debug, Clone)]
pub(crate) struct DynamicMembraneNormal {
    /// Indices of headgroup identifiers (one per molecule).
    heads: Vec<usize>,
    /// Radius of the sphere in which scan should be performed.
    radius: f32,
    /// Membrane normals calculated for each molecule.
    normals: Vec<OnceCell<Vector3D>>,
    /// Storage for membrane normals.
    storage: Option<NormalsStorage>,
}

impl DynamicMembraneNormal {
    /// Add a new molecule into the membrane normal estimator.
    #[inline(always)]
    fn insert(&mut self, molecule: &Group, system: &System) -> Result<(), TopologyError> {
        self.heads.push(get_reference_head(
            molecule,
            system,
            group_name!("NormalHeads"),
        )?);
        self.normals.push(OnceCell::new());
        Ok(())
    }

    /// Get membrane normal calculated for a molecule or calculate it it has not been already calculated.
    #[inline]
    fn get_normal<'a>(
        &mut self,
        index: usize,
        system: &'a System,
        pbc: &'a impl PBCHandler<'a>,
    ) -> Result<&Vector3D, AnalysisError> {
        let normal = self.normals.get_mut(index)
            .unwrap_or_else(||
                panic!("FATAL GORDER ERROR | DynamicMembraneNormal::get_normal | Molecule not found. Molecule with internal `gorder` index `{}` should exist. {}", 
                    index, PANIC_MESSAGE))
            .get_or_try_init(|| {
                Self::calculate_normal(*self.heads.get(index).expect(PANIC_MESSAGE), system, self.radius, pbc)
            })?;

        Ok(normal)
    }

    /// Calculate local membrane normal for the specified atom.
    // Cold because this function gets called only once per frame and molecule, but `get_normal` gets called many times more often.
    #[cold]
    fn calculate_normal<'a>(
        head: usize,
        system: &'a System,
        radius: f32,
        pbc: &'a impl PBCHandler<'a>,
    ) -> Result<Vector3D, AnalysisError> {
        let reference = system.get_atom(head)
            .unwrap_or_else(|_|
                panic!("FATAL GORDER ERROR | DynamicMembraneNormal::calculate_normal | Atom not found. Atom with index `{}` should exist. {}", 
                    head, PANIC_MESSAGE));

        let reference_pos = reference
            .get_position()
            .ok_or_else(|| AnalysisError::UndefinedPosition(reference.get_index()))?;

        // get the cloud of headgroup atoms around the reference atom; this cloud must be whole in the simulation box
        let positions = pbc.get_heads_cloud(system, reference_pos, radius)?;

        membrane_normal_from_cloud(&positions).map_err(AnalysisError::DynamicNormalError)
    }

    /// Reset membrane normals.
    #[inline(always)]
    fn reset_normals(&mut self) {
        self.normals
            .iter_mut()
            .for_each(|cell| *cell = OnceCell::new());
    }

    /// Store membrane normals, if requested.
    /// Missing membrane normals are considered to be [NAN, NAN, NAN].
    fn store_normals(&mut self) {
        if let Some(storage) = self.storage.as_mut() {
            let mut normals = Vec::with_capacity(self.heads.len());

            for raw_normal in self.normals.iter() {
                let normal = raw_normal
                    .get()
                    // if the membrane normal was not calculated for this molecule, return a vector of NANs
                    // the alternative would be to calculate it post-hoc but that is more complicated to implement
                    .unwrap_or(&Vector3D::new(f32::NAN, f32::NAN, f32::NAN))
                    .clone();
                normals.push(normal);
            }

            storage.normals.push(normals);
        }
    }

    pub(crate) fn extract_storage(&self) -> Option<Vec<Vec<Vector3D>>> {
        self.storage.as_ref().map(|x| x.normals.clone())
    }
}

impl Add for DynamicMembraneNormal {
    type Output = DynamicMembraneNormal;

    fn add(self, rhs: Self) -> Self::Output {
        // adding is only needed if storage is set
        if self.storage.is_none() {
            return self;
        }

        match (self.storage, rhs.storage) {
            (Some(x), Some(y)) => Self {
                heads: self.heads,
                radius: self.radius,
                normals: self.normals,
                storage: Some(x + y),
            },
            (_, _) => unreachable!(
                "FATAL GORDER ERROR | DynamicMembraneNormal::add | Unreachable pattern reached. {}",
                PANIC_MESSAGE
            ),
        }
    }
}

#[derive(Debug, Clone)]
pub(super) struct ManualMembraneNormal {
    normals: Option<Vec<Vec<Vector3D>>>,
    step: usize,
}

impl ManualMembraneNormal {
    /// Get membrane normal for a given molecule in a given frame.
    fn get_normal(
        &self,
        frame_index: usize,
        molecule_index: usize,
    ) -> Result<&Vector3D, AnalysisError> {
        let unwrapped = self.normals.as_ref().unwrap_or_else(||
            panic!("FATAL GORDER ERROR | ManualMembraneNormal::get_normal | Membrane normals have not been set up. {}", PANIC_MESSAGE));

        // to get the index at which the normals are stored, we need to divide
        // the actual frame_index (index of the frame in the trajectory) by analysis step
        let storage_index = frame_index / self.step;

        // defensive check: frame_index must be divisible by step (0-based index)
        assert!(
            frame_index % self.step == 0,
            "FATAL GORDER ERROR | ManualMembraneNormal::get_normal | Invalid stepping. Frame index `{}` should be divisible by step `{}`. {}", frame_index,
                self.step, PANIC_MESSAGE
        );

        Ok(unwrapped
            .get(storage_index)
            .ok_or_else(||
                AnalysisError::ManualNormalError(ManualNormalError::FrameNotFound {
                    frame_index,
                    step: self.step,
                    available_for: self.n_frames(),
                }))?
            .get(molecule_index)
            .unwrap_or_else(||
                panic!("FATAL GORDER ERROR | ManualMembraneNormal::get_normal | Molecule index `{}` not found but this should have been checked before. {}", 
                molecule_index, PANIC_MESSAGE))
        )
    }

    /// Get the number of frames specified for the normals classification.
    /// Returns 0, if the structure is not finalized.
    pub(super) fn n_frames(&self) -> usize {
        match self.normals.as_ref() {
            None => 0,
            Some(x) => x.len(),
        }
    }

    /// Get the normals for a specific molecule type and check their sanity.
    fn get_normals_for_molecule(
        normals: &HashMap<String, Vec<Vec<Vector3D>>>,
        name: &str,
        n_molecules: usize,
    ) -> Result<Vec<Vec<Vector3D>>, ManualNormalError> {
        // get the data for target molecule
        let normals_for_molecule = normals
            .get(name)
            .ok_or_else(|| ManualNormalError::MoleculeTypeNotFound(name.to_owned()))?;

        // at least one frame must be provided
        if normals_for_molecule.is_empty() {
            return Err(ManualNormalError::NoNormals(name.to_owned()));
        }

        // number of molecules must be consistent and match the system
        for (i, frame) in normals_for_molecule.iter().enumerate() {
            if frame.len() != n_molecules {
                return Err(ManualNormalError::InconsistentNumberOfMolecules {
                    expected: n_molecules,
                    molecule: name.to_owned(),
                    got: frame.len(),
                    frame: i,
                });
            }
        }

        Ok(normals_for_molecule.clone())
    }
}

impl SystemTopology {
    /// Finalize the set up of the manual membrane normals.
    pub(super) fn finalize_manual_membrane_normals(
        &mut self,
        params: &MembraneNormal,
    ) -> Result<(), ManualNormalError> {
        let normals: HashMap<String, Vec<Vec<Vector3D>>> = match params {
            MembraneNormal::FromFile(file) => {
                let string = read_to_string(file)
                    .map_err(|_| ManualNormalError::FileNotFound(file.to_owned()))?;
                serde_yaml::from_str(&string)
                    .map_err(|e| ManualNormalError::CouldNotParse(file.to_owned(), e))?
            }
            MembraneNormal::FromMap(map) => map.clone(),
            // do nothing for the other types of membrane normal specification
            _ => return Ok(()),
        };

        let mut molecule_names = Vec::new();
        let step_size = self.step_size();

        handle_moltypes!(self.molecule_types_mut(), x => {
            for molecule in x.iter_mut() {
                let normals_for_molecule = ManualMembraneNormal::get_normals_for_molecule(
                    &normals,
                    molecule.name(),
                    molecule.n_molecules(),
                )?;

                match molecule.membrane_normal_mut() {
                    MoleculeMembraneNormal::Manual(x) => {
                        x.normals = Some(normals_for_molecule);
                        x.step = step_size;
                    },
                    _ => panic!("FATAL GORDER ERROR | SystemTopology::finalize_manual_membrane_normals | Unexpected MoleculeMembraneNormal. Expected Manual."),
                }

                molecule_names.push(molecule.name().to_owned());
            }
        });

        // check that there is no additional molecule in the normals structure
        for molecule_name in normals.keys() {
            if !molecule_names.contains(molecule_name) {
                return Err(ManualNormalError::UnknownMoleculeType(
                    molecule_name.clone(),
                    molecule_names,
                ));
            }
        }

        Ok(())
    }

    /// Sanity check for manual membrane normals specification.
    /// Checks that the number of analyzed frames matches the number of frames specified in the manual normals specification.
    /// Doesn't do anything if the membrane normals are not provided manually.
    pub(super) fn validate_normals_specification(&self) -> Result<(), ManualNormalError> {
        handle_moltypes!(self.molecule_types(), x => {
            for molecule in x.iter() {
                if let MoleculeMembraneNormal::Manual(manual) = molecule.membrane_normal() {
                    if self.total_frames() != manual.n_frames() {
                        return Err(ManualNormalError::UnexpectedNumberOfFrames {
                            used_frames: manual.n_frames(),
                            analyzed_frames: self.total_frames(),
                        });
                    }
                }
            }
        });

        Ok(())
    }
}

/// Calculate membrane normal from a cloud of points using PCA.
/// **Make sure that the points are not broken at box boundaries!**
///
/// The input is a list of `Vector3D` representing the positions of lipid head groups.
/// Returns an error if fewer than 3 points are provided.
fn membrane_normal_from_cloud(points: &[Vector3D]) -> Result<Vector3D, DynamicNormalError> {
    let num_points = points.len();
    // at least three points are required
    if num_points < 3 {
        return Err(DynamicNormalError::NotEnoughPoints(num_points));
    }

    // calculate the center of the points
    let centroid = points
        .iter()
        .fold(Vector3D::new(0.0, 0.0, 0.0), |sum, p| sum + p)
        / (num_points as f32);

    // demean the coordinates
    let mut data = DMatrix::<f32>::zeros(num_points, 3);
    for (i, p) in points.iter().enumerate() {
        data[(i, 0)] = p.x - centroid.x;
        data[(i, 1)] = p.y - centroid.y;
        data[(i, 2)] = p.z - centroid.z;
    }

    // perform singular value decomposition
    let svd = SVD::new(data, true, true);

    let v_t = match svd.v_t.as_ref() {
        Some(x) => x,
        None => return Err(DynamicNormalError::SVDFailed),
    };
    let n_cols = v_t.nrows();

    // get the last principal direction, which is the normal estimate
    Ok(Vector3D::new(
        v_t[(n_cols - 1, 0)],
        v_t[(n_cols - 1, 1)],
        v_t[(n_cols - 1, 2)],
    )
    .to_unit())
}

/// Structure used to store membrane normals calculated during the analysis.
#[derive(Debug, Clone)]
pub(crate) struct NormalsStorage {
    /// Membrane normals calculated for each molecule in each frame.
    normals: Vec<Vec<Vector3D>>,
    /// Number of threads that the data have been collected by.
    n_threads: usize,
}

impl Default for NormalsStorage {
    fn default() -> Self {
        NormalsStorage {
            normals: Vec::new(),
            n_threads: 1,
        }
    }
}

impl Add for NormalsStorage {
    type Output = Self;

    fn add(self, rhs: Self) -> Self::Output {
        if self.normals.is_empty() {
            return rhs;
        }

        if rhs.normals.is_empty() {
            return self;
        }

        let normals =
            interleave_vectors(&self.normals, &rhs.normals, self.n_threads, rhs.n_threads);

        NormalsStorage {
            normals,
            n_threads: self.n_threads + rhs.n_threads,
        }
    }
}

impl AddAssign for NormalsStorage {
    fn add_assign(&mut self, rhs: Self) {
        if self.normals.is_empty() {
            self.normals = rhs.normals;
            return;
        }

        self.normals =
            interleave_vectors(&self.normals, &rhs.normals, self.n_threads, rhs.n_threads);

        self.n_threads += rhs.n_threads;
    }
}

